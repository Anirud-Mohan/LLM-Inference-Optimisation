"""LLM-as-a-judge evaluation for the Qwen moral story pipeline.

Usage:
    python llm_as_eval.py

Required in .env:
    POD_1_URL       - deployed model endpoint
    MODEL_NAME      - model name (e.g. Qwen/Qwen2.5-1.5B-Instruct)
    JUDGE_API_KEY   - OpenAI API key
    JUDGE_MODEL     - judge model (e.g. gpt-5-mini-2025-08-07)
"""

import json
import os
from pathlib import Path

from dotenv import load_dotenv
from openai import OpenAI

load_dotenv()

POD_URL    = os.environ["POD_1_URL"].rstrip("/")
MODEL_NAME = os.getenv("MODEL_NAME", "Qwen/Qwen2.5-1.5B-Instruct")
JUDGE_MODEL = os.getenv("JUDGE_MODEL", "gpt-5-mini-2025-08-07")

model_client = OpenAI(api_key="none", base_url=f"{POD_URL}/v1")
judge_client = OpenAI(api_key=os.environ["JUDGE_API_KEY"])

JUDGE_PROMPT = """\
You are evaluating a children's moral story generated by an AI.

Prompt given to the AI:
{prompt}

Story generated by the AI:
{story}

Rate the story on these four dimensions (each 0.0 to 1.0):
- moral_clarity: does it convey a clear moral lesson?
- age_appropriateness: is it suitable for children aged 4-10?
- narrative_coherence: does it have a clear beginning, middle, and end?
- relevance: does it match the prompt's character, moral value, and setting?

Return ONLY a JSON object like this, nothing else:
{{
  "moral_clarity": YOUR_SCORE_HERE,
  "age_appropriateness": YOUR_SCORE_HERE,
  "narrative_coherence": YOUR_SCORE_HERE,
  "relevance": YOUR_SCORE_HERE,
  "reason": "one sentence summary"
}}"""


def generate_story(prompt: str) -> str:
    response = model_client.chat.completions.create(
        model=MODEL_NAME,
        messages=[{"role": "user", "content": prompt}],
        max_tokens=200,
    )
    return response.choices[0].message.content.strip()


def judge_story(prompt: str, story: str) -> dict:
    response = judge_client.chat.completions.create(
        model=JUDGE_MODEL,
        messages=[{"role": "user", "content": JUDGE_PROMPT.format(prompt=prompt, story=story)}],
        max_completion_tokens=4000,
    )
    choice = response.choices[0]
    raw = (choice.message.content or "").strip()
    print(f"  [finish_reason={choice.finish_reason}  usage={response.usage}]")
    print(f"  [judge raw] {raw[:400]}")
    start, end = raw.find("{"), raw.rfind("}")
    if start != -1 and end != -1:
        return json.loads(raw[start : end + 1])
    raise ValueError(f"Judge returned no JSON: {raw!r}")


def main():
    with open("eval_data.json") as f:
        eval_data = json.load(f)

    Path("results").mkdir(exist_ok=True)
    results = []

    print(f"Evaluating {len(eval_data)} prompts | model={MODEL_NAME} | judge={JUDGE_MODEL}\n")

    for i, item in enumerate(eval_data, 1):
        prompt = item["prompt"]
        print(f"[{i}/{len(eval_data)}] {prompt[:80]}...")

        story = generate_story(prompt)
        print(f"  [story]  {story[:120]}...")

        scores = judge_story(prompt, story)
        avg = sum(scores[k] for k in ("moral_clarity", "age_appropriateness", "narrative_coherence", "relevance")) / 4
        print(f"  [scores] avg={avg:.2f}  {scores.get('reason', '')}\n")

        results.append({"prompt": prompt, "story": story, "scores": scores, "avg": round(avg, 4)})

    out_path = "results/eval_results.json"
    with open(out_path, "w") as f:
        json.dump(results, f, indent=2)

    avgs = [r["avg"] for r in results]
    print(f"Done. Overall avg score: {sum(avgs)/len(avgs):.4f}")
    print(f"Results saved to {out_path}")


if __name__ == "__main__":
    main()
